                  <h3>Як дізнатися, які частоти канали найменш завантажені в місці,&nbsp;в якому вам потрібно
                    використовувати Wi-Fi?</h3>
                  <p>Багато користувачів, коли у них вдома перестає або починає погано працювати WiFi, грішать на
                    провайдера або роутер-точку доступу. Однак, найчастіше проблеми з якістю нам створюють бездротові
                    Wi-Fi мережі сусідів. Сусіди купують собі Wi-Fi роутери і починають працювати на каналах, які
                    встановлені за умовчанням, а вони часто збігаються з нашими. Як правило це 6-й канал.&nbsp;</p>
                  <p>Якщо спрощено, чим більше Wi-Fi точок на каналі (чим більше точок ділять одну частоту), тим більше
                    вони один одному заважають, тим нижче швидкість. Так що треба вибирати канал найменш навантажений, в
                    ідеалі &mdash; вільний.</p>
                  <p>Так як же дізнатися які канали Wi-Fi вільні біля вашої квартири, а які найбільш завантажені?</p>
                  <p>Уявімо ситуацію: кілька WiFi модем-роутерів займають однакову частоту (канал). Виникає
                    інтерференція радіосигналів бездротових мереж, яка є найголовнішою причиною проблем з Wi-Fi точками
                    доступу і поганим зв'язком між WiFi пристроями і точкою доступу. Сильно падає швидкість обміну
                    даними з точкою доступу, швидкість роботи в інтернет, а іноді взагалі не можливо увійти в свою
                    бездротову мережу.</p>
                  <p><strong>Як же вирішити цю проблему?</strong></p>
                  <p>Дуже просто. Треба завантажити безкоштовну утиліту <strong>InSSIDer</strong>, яка розшукує всі
                    працюючі Wi-Fi точки доступу в околиці і надає в зручному вигляді всю інформацію про них: канал, на
                    якому йде обмін даними, частоти роботи, виробник обладнання, MAC-адреси всіх найближчих точок
                    доступу, SSiD, назва бездротової мережі, максимально можлива швидкість з'єднання, вид шифрування і
                    багато іншого. Все це видається у вигляді наочних графіків.</p>
                  <p><img title="все про WiFI мережі" src="@/assets/img/manual/internet_wifi_man/wifi_analizer.webp"
                          alt="вся інформація по всіх WiFi мереж наочно " width="80%"
                          longdesc="http://www.broadband.org.ua/ot-redaktsii/%D0%9F%D0%BE%D0%BB%D0%BD%D0%B0%D1%8F%20%D0%B8%D0%BD%D1%84%D0%BE%D1%80%D0%BC%D0%B0%D1%86%D0%B8%D1%8F%20%D0%BF%D1%80%D0%BE%20WiFI%20%D1%81%D0%B5%D1%82%D0%B8%20%D0%B2%20%D0%B2%D0%B0%D1%88%D0%B5%D0%BC%20%D0%B4%D0%BE%D0%BC%D0%B5"/>
                  </p>
                  <p>Запустіть цю програму на пристрої з WiFi наприклад, на ноутбуці. І скануєте всі навколишні
                    бездротові мережі..</p>
                  <p>Отримані дані особливо завантаження каналів &nbsp;використовуємо для вибору вільних діапазонів. У
                    нас на графіку це канал 2 на частоті 2.4 GHz, на якій і працює мережа автора.</p>
                  <p><img src="@/assets/img/manual/internet_wifi_man/wifi_analizer_free_chainel.webp" alt="chanel free manual 3" width="80%" /></p>
                  <p><strong>Як перевести роутер на вільні канали?</strong></p>
                  <p>Заходимо в налаштування точки доступу через браузер (дивитися в інтфрукціі до роутера, але це як
                    правило 192.168.0.1 або 192.168.1.1, вводимо паролі доступу), після чого шукаємо в роутері
                    налаштування Wireless Channel (бездротові канали). Після зміни каналів на вільні, зазвичай швидкість
                    роботи інтернету у всіх бездротових пристроях в квартирі значно зростає.</p>
                  <p>Дуже привабливо виглядає <strong>діапазон в 5 ГГц</strong>. так як він майже завжди вільний. Там
                    каналів набагато більше, та й ефір чистіше, але для переходу на даний діапазон всі пристрої Вашій
                    мережі повинні його підтримувати. На жаль, підтримка 5 ГГц поки зустрічається рідко, але є
                    маршрутизатори з підтримкою одночасної трансляції обох частот <strong>2,4 ГГц.&nbsp;і
                      5,0&nbsp;</strong><strong>ГГц</strong>.</p>
