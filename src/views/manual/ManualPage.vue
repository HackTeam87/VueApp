                  <h3>Реальна швидкість з'єднання, яка використовується в технології WI-FI</h3>
                  <p><strong>Чому швидкість передачі даних по Wi-Fi нижче обіцяної швидкості 300 Мбіт/с (або 150
                    Мбіт/с)?</strong></p>
                  <p>В технічних характеристиках будь-якого роутера вказані бездротові можливості роутера, швидкість
                    wi-fi. Найпоширеніші це 150 Мбіт/сек і 300 Мбіт/сек.</p>
                  <p><strong>300 Мбіт/с</strong> - максимальна швидкість роботи на фізичному рівні за стандартом IEEE
                    802.11n при з'єднанні з адаптерами, що використовують <strong>два</strong> просторових потоку і
                    канал 40 МГц для прийому і передачі. Дійсна швидкість передачі даних в бездротовій мережі залежить
                    від особливостей і налаштувань клієнтського обладнання, числа клієнтів в мережі, перешкод на шляху
                    проходження сигналу, а також наявності інших бездротових мереж і радіоперешкод в тому ж діапазоні.
                  </p>
                  <p><strong>150 Мбіт/с</strong> - максимальна швидкість роботи на фізичному рівні за стандартом IEEE
                    802.11n при з'єднанні з адаптерами, що використовують <strong>один</strong> просторовий потік і
                    канал 40 МГц для прийому і передачі (при використанні каналу 20 МГц швидкість буде не вище 72
                    Мбіт/с).</p>
                  <p>Почнемо з того, що багато користувачів невірно орієнтуються на швидкість підключення в мегабітах в
                    секунду (Мбіт/с), яке відображається в рядку <strong>Швидкість</strong> (Speed) на закладці <strong>Загальні</strong>
                    (General) у вікні <strong>Стан</strong> (Status) бездротового з'єднання в операційній системі
                    Windows.</p>
                  <p>Дана цифра показує швидкість яка використовується в даний час в рамках обраного стандарту. А
                    реальна швидкість передачі даних нижче канальної приблизно в 2-3 рази. Різниця між швидкістю
                    підключення і реальними показниками пояснюється перш за все великим обсягом службових даних,
                    втратами мережевих пакетів в бездротовому середовищі і витратами на повторну передачу. Реальна
                    швидкість також залежить від налаштувань точки доступу, числа одночасно підключених до неї
                    клієнтських бездротових адаптерів та інших факторів.</p>
